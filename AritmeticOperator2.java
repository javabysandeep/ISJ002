public class AritmeticOperator2 {
	
	public static void main(String[] args){
		
		System.out.println(11.0f / 2);//5.5 
		System.out.println(10.0 % 2); //0.0
	
		System.out.println(1.0 / 10);//0.0
		System.out.println(1.0 % 10); //1.0

		System.out.println(0.0 / 10);//0.0
		System.out.println(0.0 % 10); //0.0

		System.out.println(1.0 / 0);// Infinity
		System.out.println(1.0 % 0); // NaN : Not A Number

		System.out.println('A' / 10);//6
		System.out.println('A' % 10); //5
	
		
	}

}