public class DataType {

	public static void main(String[] args) {
		byte value1 = 1;
		short value2 = 2;
		int value3 = 3;
		long  value4 = 4L;
		float value5 = 100.3F;
		double value6 = 100000.8989D;
		char value7  = 'B';
		boolean value8 = true;

	System.out.println("Byte value "+value1);
	System.out.println("Short value "+value2);
	System.out.println("Int value "+value3);
	System.out.println("Long value "+value4);
	System.out.println("float value "+value5);
	System.out.println("double value "+value6);
	System.out.println("Character value "+value7);
	System.out.println("boolean value "+value8);

 
	
	}

}